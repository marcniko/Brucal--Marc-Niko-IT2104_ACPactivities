class Capybara:
    def __init__(self, name: str, gender: str, age: int):
        """
        Initializes a new Capybara instance.

        :param name: Name of the capybara
        :param gender: Gender of the capybara ('M' for male, 'F' for female)
        :param age: Age of the capybara
        """
        self.name = name
        self.gender = gender
        self.age = age