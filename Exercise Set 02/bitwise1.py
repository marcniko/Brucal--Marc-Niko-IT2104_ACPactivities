# a. 12 | 25 (Bitwise OR)
a = 12 | 25

# b. 21 & 27 (Bitwise AND)
b = 21 & 27

# c. 8 ^ 7 (Bitwise XOR)
c = 8 ^ 7

# d. 13 & 7 (Bitwise AND)
d = 13 & 7

# e. 13 | 7 (Bitwise OR)
e = 13 | 7

# Display the results
print(f"a. 12 | 25 = {a}")
print(f"b. 21 & 27 = {b}")
print(f"c. 8 ^ 7 = {c}")
print(f"d. 13 & 7 = {d}")
print(f"e. 13 | 7 = {e}")

